package main;

/**
 *
 * @author Guilherme Muniz
 */
public class Pessoa {

    private String nome;
    private int cpf;
    private int rg; 
    private int idade;

    public Pessoa(){
        
    }
    
    
    
    public Pessoa(String nome, int cpf, int rg, int idade){
        
        this.cpf = cpf;
        this.idade = idade;
        this.rg = rg;
        this.nome = nome;
        
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the cpf
     */
    public int getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    /**
     * @return the rg
     */
    public int getRg() {
        return rg;
    }

    /**
     * @param rg the rg to set
     */
    public void setRg(int rg) {
        this.rg = rg;
    }

    /**
     * @return the idade
     */
    public int getIdade() {
        return idade;
    }

    /**
     * @param idade the idade to set
     */
    public void setIdade(int idade) {
        this.idade = idade;
    }
    
    
    
    
    
}
